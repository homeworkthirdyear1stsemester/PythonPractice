print('or')
print(0 | 0)
print(0 | 1)
print(1 | 0)
print(1 | 1)

print('and')
print(0 & 0)
print(0 & 1)
print(1 & 0)
print(1 & 1)

print('xor')
print(0 ^ 0)
print(0 ^ 1)
print(1 ^ 0)
print(1 ^ 1)

print('not')
print(bin(0))
print(bin(~0))
print(bin(1))
print(bin(~1))  # 2의 보수

print('shift')
print(1 << 0)
print(1 << 1)
print(1 >> 0)
print(1 >> 1)
